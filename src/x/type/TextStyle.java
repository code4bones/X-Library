package x.type;

import x.ui.XUITextView;

/**
 * @brief The text style class that is used with {@link XUITextView}
 */
public class TextStyle
{
	/**
	 * Sets the text style as bold
	 */
	public static final int BOLD = 0x001;
	
	/**
	 * Sets the text style as italic
	 */
	public static final int ITALIC = 0x010;
	
	/**
	 * Sets the text style as underline
	 */
	public static final int UNDERLINE = 0x100;
}
